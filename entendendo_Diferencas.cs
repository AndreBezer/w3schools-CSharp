using System;

namespace Entendendo_Diferenca_Entre_WriteLine
{

    // Entendendo a diferenca entre
    // Comandos WriteLine
    // E comandos Write

    public class Program
    {
        public static void Main(string[] args)
        {
            //WriteLine
            Console.WriteLine("Este é um ");
            Console.WriteLine("Comando WriteLine ");
            Console.WriteLine("Legal...");

            //Write
            Console.Write("Este é um ");
            Console.Write("Comando Write, ");
            Console.Write("Show ");
        }
    }
}
